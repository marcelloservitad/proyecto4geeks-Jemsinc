<h1> Tom Clancy's:The Division</h1>
	<h2> La nueva IP de Electronic Arts </h2>
  	<br>
  	<br>
  	</div>
     <h4 class="content">Sin duda, Tom Clancy’s The Division se convirtió en uno de los juegos más esperados desde que vimos su anuncio en la conferencia de prensa de Ubisoft en E3 2013.

Su estilo que combina un third person shooter con elementos de RPG en un detallado ambiente urbano es algo que atrapó la atención de muchos gamers, quienes esperamos con ansias la salida del título.

Después de varios retrasos, finalmente está disponible The Division y, aunque en buena parte de los aspectos cumple con lo prometido, definitivamente hay cosas que mejorar.

El juego se desarrolla en un mundo abierto, en este caso, una ciudad de Nueva York completamente devastada por una epidemia de viruela liberada durante un Black Friday a través de billetes. Tú eres parte de la Strategic Homeland Division o, simplemente The Division, una organización formada por agentes encubiertos entre lo que queda de la población en Nueva York cuya tarea es devolver el orden y restaurar la ciudad. 

El juego comienza lento y básicamente te da misiones para que puedas familiarizarte mejor con el sistema de juego. Hay que decir, no es un juego tan intuitivo como otros, definitivamente tiene una curva de aprendizaje pero en unas horas estarás dominándolo.

Visualmente el juego se ve muy bien y es una de las mejores cosas en The Division, la ciudad está muy detallada, con toda clase de objetos que muestran la devastación de Nueva York y un buen ciclo de día y noche con cambios en el clima y efectos que lucen muy bien, aunque de pronto puede haber algunos glitches muy extraños pero no son la norma. En los juegos de mundo abierto, esto es muy común. </h3>
<br>
<img src="img/tom_clancy1.png" class="image">
<br>
<h4 class="content">El juego se ve bien en consolas (nosotros probamos la versión de PS4) pero si tienen PC podrán ver un mundo aún más detallado.

Cuando comenzó a funcionar el juego, hubo muchos problemas con los servidores y gente que no pudo jugar por días. Aún cuando de pronto llega ha haber problemas con los servidores, son cada vez más raros y, por lo general, es muy sencillo volver a la partida.

El gameplay en general es bueno, tiene un buen sistema de loot en el que podemos encontrar armas útiles pronto y a veces cosas que parecerían inútiles como ropa, sin embargo, es posible deshacerlos para obtener materiales que nos permitan crear ítems. El sistema de customización de armas también es bueno y es otro de los mejores aspectos del juego.

division7

Las situaciones en el juego se pueden poner muy intensas rápidamente y realmente puede llegar a ser frustrante. Aún cuando es completamente posible jugar solo en The Division, la experiencia está hecha para jugar cooperativamente con amigos donde se pueden organizar tácticamente y tener una experiencia muchísimo más divertida en el juego.
<br>
<img src="img/tom_clancy3.png" class="image">
<br>
Tristemente, el juego también tiene sus fallas. El sistema de cobertura no está muy bien logrado y no siempre es muy responsivo. La historia no es mala pero los personajes no son muy memorables. La variedad de enemigos también deja un poco que desear y hay algunos jefes que realmente no son diferentes de los enemigos básicos y puede llegar a ser tedioso lidiar con ellos.

En cuanto terminamos la historia comienza Endgame y lamentablemente también hay muchos problemas de balance, sin mencionar la Dark Zone en la que el juego se convierte en un multiplayer PvP en la que también imperan algunos problemas y volverse rogue puede ser bastante frustrante.

Sin embargo, Ubisoft tiene conocimiento de varios de estos problemas y, recientemente, han mencionado que la Dark Zone y otros aspectos serán mejorados muy pronto.

TThe Division sin duda tiene sus problemas pero los aspectos que hace bien, los ejecuta de gran manera. Uno de los mejores aspectos de que este sea un juego online es que los aspectos que fallan pueden ser mejorados y parece ser que no habrá que esperar mucho para que así sea.</h4>
<br>
<img src="img/tom_clancy2.png" class="image">
<br>
<h4 class="content">Por lo demás, es un juego entretenido, especialmente si tienen amigos con quienes jugarlo y esa es básicamente la idea del juego. The Division puede mejorar pero sus cimientos son lo suficientemente fuertes como para que valga la pena checarlo.
</h4>


